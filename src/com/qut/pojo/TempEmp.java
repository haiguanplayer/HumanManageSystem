package com.qut.pojo;

public class TempEmp {

	private int p_id;
	private String p_name;
	private String entry_date;
	private int try_time;
	private String qiman_time;
	public int getP_id() {
		return p_id;
	}
	public void setP_id(int p_id) {
		this.p_id = p_id;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getEntry_date() {
		return entry_date;
	}
	public void setEntry_date(String entry_date) {
		this.entry_date = entry_date;
	}
	public int getTry_time() {
		return try_time;
	}
	public void setTry_time(int try_time) {
		this.try_time = try_time;
	}
	public String getQiman_time() {
		return qiman_time;
	}
	public void setQiman_time(String qiman_time) {
		this.qiman_time = qiman_time;
	}
	
	
}
