package com.qut.pojo;


public class Post {
	private int post_id;
	private String post_name;
	private String post_type;
	private int post_max;
	public int getPost_id() {
		return post_id;
	}
	public void setPost_id(int postId) {
		post_id = postId;
	}
	public String getPost_name() {
		return post_name;
	}
	public void setPost_name(String postName) {
		post_name = postName;
	}
	public String getPost_type() {
		return post_type;
	}
	public void setPost_type(String postType) {
		post_type = postType;
	}
	public int getPost_max() {
		return post_max;
	}
	public void setPost_max(int postMax) {
		post_max = postMax;
	}
	
	
}
