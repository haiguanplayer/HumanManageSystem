package com.qut.pojo;




public class Dept {
	
	
	private int dept_id;
	private String dept_name;
	private String dept_class;
	private String dept_phone;
	private String dept_build_date;
	
	private int count;
	
	public int getDept_id() {
		return dept_id;
	}
	public void setDept_id(int dept_id) {
		this.dept_id = dept_id;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public String getDept_class() {
		return dept_class;
	}
	public void setDept_class(String dept_class) {
		this.dept_class = dept_class;
	}
	public String getDept_phone() {
		return dept_phone;
	}
	public void setDept_phone(String dept_phone) {
		this.dept_phone = dept_phone;
	}
	public String getDept_build_date() {
		return dept_build_date;
	}
	public void setDept_build_date(String dept_build_date) {
		this.dept_build_date = dept_build_date;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
	
	
}
